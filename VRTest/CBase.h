/*******************************//*!
	@brief	基底クラス
*//********************************/
class CBase
{

public:
	
	/*************************************************************************//*!
		@brief			コンストラクタ
		@param			None		

		@return			None
	*//**************************************************************************/
	CBase(){};

	/*************************************************************************//*!
		@brief			デストラクタ
		@param			None		

		@return			None
	*//**************************************************************************/
	virtual ~CBase(){};

	/*************************************************************************//*!
		@brief			作成
		@param			None		

		@return			None
	*//**************************************************************************/
	virtual void Create() = 0;

	/*************************************************************************//*!
		@brief			初期化
		@param			None		

		@return			None
	*//**************************************************************************/
	virtual void Initialize() = 0;

	/*************************************************************************//*!
		@brief			更新
		@param			None		

		@return			None
	*//**************************************************************************/
	virtual void Update() = 0;

	/*************************************************************************//*!
		@brief			描画
		@param			None		

		@return			None
	*//**************************************************************************/
	virtual void Render() = 0;

	/*************************************************************************//*!
		@brief			解放 
		@param			None		

		@return			None
	*//**************************************************************************/
	virtual void Release() = 0;
};