//NONE
#pragma once
 
//PROTOTYPE
class PmObject;

/*******************************//*!
	@brief	PmObjectの動作検知用基底クラス

			PmObjectに持たせ、アクターの動作に応じて呼び出される

*//********************************/
class PmCollisionSearch
{
public:

	/*************************************************************************//*!
		@brief			コンストラクタ
		@param			None		

		@return			None
	*//**************************************************************************/
	PmCollisionSearch(){};

	/*************************************************************************//*!
		@brief			デストラクタ
		@param			None		

		@return			None
	*//**************************************************************************/
	virtual ~PmCollisionSearch(){};

	/*************************************************************************//*!
		@brief			衝突検知

						オブジェクト同士が衝突した時に呼ばれる

		@param[obj]		衝突したオブジェクトのポインタ		

		@return			None
	*//**************************************************************************/
	virtual void onContact(PmObject* obj){};

	/*************************************************************************//*!
		@brief			重なり検知

						オブジェクト同士が重なった時<br>
						重なっていたオブジェクトが離れる時に呼ばれる

		@param[obj]		衝突したオブジェクトのポインタ		

		@return			None
	*//**************************************************************************/
	virtual void onTrigger(PmObject* obj){};

	/*************************************************************************//*!
		@brief			未実装
	*//**************************************************************************/
	virtual void onExit(PmObject* obj){};

	/*************************************************************************//*!
		@brief			未実装
	*//**************************************************************************/
	virtual void onWake(){};

	/*************************************************************************//*!
		@brief			未実装
	*//**************************************************************************/
	virtual void onSleep(){};
};